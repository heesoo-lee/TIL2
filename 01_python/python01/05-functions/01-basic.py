def make_sum(pram1, pram2):
    """이것은 두 수를 받아
    두 수의 합을 반환하는 함수입니다.
    >>> make_sum(1, 2)
    3
    """
    return pram1 + pram2

# 입력값 = parameter
# 반환값 = return value
# return 값 없으면 None 반환

# 일급 객체
# 함수가 변수에 할당될 수 있음
# 함수가 다른 함수의 인자로 전달될 수 있음
# 함수가 다른 함수에 의해 반환될 수 있음
# 함수도 객체

# 매개변수 (parameter) vs 인자 (argument
# 매개변수는 함수 정의 시 사용되는 변수 이름
# 인자는 함수를 호출할 때 전달되는 값